def my_first_interpreter(code):
    # Make your esolang interpreter here
    count = 0
    res = ''
    for i in code:
        if i not in ['+', '.']:
            continue
        if i == '+':
            count = (count % 256) + 1
        else:
            res += chr(count)
    return res


